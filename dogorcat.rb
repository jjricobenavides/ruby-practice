puts "What is your animal? Please enter in lowercase to listen."
answer = gets.chomp
if answer == 'dog'
    puts 'woof'
    elsif answer == 'cow'
    puts 'mooooo'
    elsif answer == 'sheep'
    puts 'mheeeee'
    elsif answer == 'cat' 
    puts 'meow'
else 
    puts "we don't have that animal sound, sorry!"
end 
